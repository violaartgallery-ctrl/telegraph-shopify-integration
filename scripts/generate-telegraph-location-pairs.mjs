import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(
  root,
  "shopify-theme",
  "managed",
  "assets",
  "telegraph-locations.json",
);
const outputPath = path.join(
  root,
  "extensions",
  "require-telegraph-location",
  "src",
  "generated_location_pairs.js",
);

const parsed = JSON.parse(await readFile(sourcePath, "utf8"));
const locations = parsed.locations;
if (!Array.isArray(locations)) {
  throw new Error("telegraph-locations.json must contain a locations array");
}

const pairs = locations.flatMap((governorate) => {
  if (!Number.isInteger(governorate.id) || !Array.isArray(governorate.subzones)) {
    throw new Error(`Invalid governorate entry: ${JSON.stringify(governorate)}`);
  }
  return governorate.subzones.map((area) => {
    if (!Number.isInteger(area.id)) {
      throw new Error(`Invalid area entry: ${JSON.stringify(area)}`);
    }
    return `${governorate.id}:${area.id}`;
  });
});

if (pairs.length !== new Set(pairs).size) {
  throw new Error("Duplicate governorate/area pairs detected");
}

const output =
  "// Generated by scripts/generate-telegraph-location-pairs.mjs.\n" +
  "// Source: shopify-theme/managed/assets/telegraph-locations.json\n" +
  `export const VALID_TELEGRAPH_LOCATION_PAIRS = Object.freeze(${JSON.stringify(pairs, null, 2)});\n`;

await writeFile(outputPath, output, "utf8");
console.log(`Generated ${pairs.length} valid Telegraph location pairs.`);
